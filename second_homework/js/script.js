let num1 = prompt('Enter your first number !')
let op  = prompt('Enter opertator!')
let num2 = prompt('Enter your second number')

if(op =='+'){
    console.log(+num1 + +num2)
}else if (op == '-'){
    console.log(num1-num2)
}else if (op =='*'){
    console.log(num1*num2)
}else if (op == '/'){
    console.log(num1/num2)
}